/** Hook patterns + thread skeleton for CRAFT inject. */
export const CRAFT_HOOKS = `HOOK PATTERNS (first ~7 words stop the scroll):
1. Primary-source quote — lead with a verifiable line, then frame it.
2. "Isn't X. It's Y." reframe — subvert the obvious in ≤12 words.
3. Specific number — anchor with a hard figure + why it matters.
4. Forced binary — Either A or B. Which one?
5. Then vs now — side-by-side contrast + closing principle.
6. Industry comparison — what competitors famously don't do.
7. Personal narrative — effort/credibility, then payoff line.
8. Pattern recognition — design choice transferred across domains.
9. Stolen trick — name the technique + outcome; "I'm stealing this."
10. Negative observation — list what's missing + forced choice.

Avoid: "I just realized…", "Hot take:", "Unpopular opinion:", "RT if you agree", question hooks with no payoff, SEO listicle openers.

THREAD SKELETON (5 beats):
1 Hook — strongest claim (must stand alone)
2 Setup — why the reader should care
3 Meat — most surprising / valuable insight
4 Elaboration — implications or receipts
5 Close — principle + reply prompt / CTA
Do not bury the lede in beat 4.`
